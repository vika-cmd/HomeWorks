package oop1.mind.obertka;

public class ObertkaMAX {
    public static void main(String[] args) {
        /*
        Наибольшее целое число равно 2147483647
        Наименьшее положительное число типа float равно 1.4E-45
        Наибольшее число типа double равно 1.7976931348623157E308
         */
        System.out.println("Наибольшее целое число равно "
                + Integer.MAX_VALUE);
        System.out.println("Наименьшее положительное число типа float равно "
                + Float.MIN_VALUE);
        System.out.println("Наибольшее число типа double равно "
                + Double.MAX_VALUE);

    }
}
